								<p className="hdng-16">A mission statement describes the purpose and day-to-day effort of an organization. A vision statement describes an organization’s ultimate goal or future outcome. But the two work in tandem: A vision statement expresses what you ultimately want to achieve; a mission statement articulates what you will do to get there.</p>
